async function seed() {
  console.log('Seeding 20 high-fidelity calendar entries into Supabase...');
  for (const entry of calendarEntries) {
    try {
      const response = await fetch(`${supabaseUrl}/rest/v1/spiritual_calendar`, {
        method: 'POST',
        headers: {
          'apikey': supabaseKey,
          'Authorization': `Bearer ${supabaseKey}`,
          'Content-Type': 'application/json',
          'Prefer': 'resolution=merge-duplicates'
        },
        body: JSON.stringify(entry)
      });
      
      if (!response.ok) {
        const errText = await response.text();
        console.error(`Failed for ${entry.date_key}: ${response.status} - ${errText}`);
      } else {
        console.log(`Success seeding event on ${entry.date_key}`);
      }
    } catch (err) {
      console.error(`Network error for ${entry.date_key}:`, err);
    }
  }
}

